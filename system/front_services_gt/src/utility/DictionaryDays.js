

function DictionaryDays(ind){
     ind=Number(ind);
     if(ind==1){return "Saturday"}
     else if(ind==2){return "Sunday"}
     else if(ind==3){return "Monday"}
     else if(ind==4){return "Tuesday"}
     else if(ind==5){return "Wednesday"}
     else if(ind==6){return "Thursday"}
     else if(ind==7){return "Friday"}
}

export default DictionaryDays